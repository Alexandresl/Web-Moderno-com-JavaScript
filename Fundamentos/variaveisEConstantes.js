// Variáveis
var a = 3;
let b = 4;

var a = 30; // var permite que uma mesma variável seja redeclarada, embora não seja recomendado
b = 40; // let não permite redeclaração

console.log(a, b);

// Modificando os valores
a = 300;
b = 400;

console.log(a, b);

// Declarando uma constante e imprimindo seu valor
const c = 5;

console.log(c);